package com.michalgarnczarski;

public class LockCasette {
    private int width;
    private int offset;

    public LockCasette(int width, int offset) {
        this.width = width;
        this.offset = offset;
    }

    public int getWidth() {
        return width;
    }

    public int getOffset() {
        return offset;
    }
}
